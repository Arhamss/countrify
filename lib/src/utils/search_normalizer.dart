/// Small utilities for normalizing user-entered search queries.
///
/// The defaults applied by `GeoItemPicker` are:
///
/// 1. `trim()`
/// 2. `toLowerCase()`
/// 3. Accent-fold (when `GeoPickerConfig.accentInsensitiveSearch` is on).
///
/// Accent folding maps Latin-extended diacritics to their ASCII base letters
/// so queries like `sao paulo` match `São Paulo` and `karlovy vary` matches
/// `Karlový Vary`. The table is intentionally small — a few hundred bytes —
/// and covers the diacritics present in the bundled dr5hn dataset.
abstract final class SearchNormalizer {
  /// Trim + lowercase only.
  static String basic(String input) => input.trim().toLowerCase();

  /// Trim + lowercase + accent-fold.
  static String foldAccents(String input) {
    final lower = input.trim().toLowerCase();
    if (lower.isEmpty) return lower;
    final buffer = StringBuffer();
    for (final code in lower.codeUnits) {
      buffer.writeCharCode(_foldTable[code] ?? code);
    }
    return buffer.toString();
  }

  /// Lookup of lower-cased Latin-extended code points to their ASCII folded
  /// equivalent. Characters with multi-char folds (e.g. `ß → ss`, `æ → ae`)
  /// are not represented here; they are rare in place names and the cost of
  /// shipping a full Unicode normalization database outweighs the benefit.
  static const Map<int, int> _foldTable = {
    // Latin-1 supplement (à-ÿ)
    0xE0: 0x61, 0xE1: 0x61, 0xE2: 0x61, 0xE3: 0x61, 0xE4: 0x61, 0xE5: 0x61,
    0xE7: 0x63,
    0xE8: 0x65, 0xE9: 0x65, 0xEA: 0x65, 0xEB: 0x65,
    0xEC: 0x69, 0xED: 0x69, 0xEE: 0x69, 0xEF: 0x69,
    0xF1: 0x6E,
    0xF2: 0x6F, 0xF3: 0x6F, 0xF4: 0x6F, 0xF5: 0x6F, 0xF6: 0x6F, 0xF8: 0x6F,
    0xF9: 0x75, 0xFA: 0x75, 0xFB: 0x75, 0xFC: 0x75,
    0xFD: 0x79, 0xFF: 0x79,
    // Latin extended-A selection (common in Eastern European place names)
    0x101: 0x61, 0x103: 0x61, 0x105: 0x61,
    0x107: 0x63, 0x109: 0x63, 0x10D: 0x63,
    0x10F: 0x64, 0x111: 0x64,
    0x113: 0x65, 0x115: 0x65, 0x117: 0x65, 0x119: 0x65, 0x11B: 0x65,
    0x11F: 0x67, 0x121: 0x67, 0x123: 0x67,
    0x125: 0x68,
    0x129: 0x69, 0x12B: 0x69, 0x12D: 0x69, 0x12F: 0x69, 0x131: 0x69,
    0x135: 0x6A,
    0x137: 0x6B,
    0x13A: 0x6C, 0x13C: 0x6C, 0x13E: 0x6C, 0x142: 0x6C,
    0x144: 0x6E, 0x146: 0x6E, 0x148: 0x6E,
    0x14D: 0x6F, 0x14F: 0x6F, 0x151: 0x6F,
    0x155: 0x72, 0x157: 0x72, 0x159: 0x72,
    0x15B: 0x73, 0x15D: 0x73, 0x15F: 0x73, 0x161: 0x73,
    0x163: 0x74, 0x165: 0x74, 0x167: 0x74,
    0x169: 0x75, 0x16B: 0x75, 0x16D: 0x75, 0x16F: 0x75, 0x171: 0x75, 0x173: 0x75,
    0x175: 0x77,
    0x177: 0x79,
    0x17A: 0x7A, 0x17C: 0x7A, 0x17E: 0x7A,
  };
}
